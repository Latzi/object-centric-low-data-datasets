# ============================================================
# PATH RESOLUTION AND VALIDATION
# ============================================================

def resolve_paths(args):
    if not args.local_cropped_root:
        raise RuntimeError(
            "Local cropped root was not provided. Pass --local-cropped-root "
            "or set COCO_LOCAL_CROPPED_ROOT."
        )

    if not args.local_full_yolo_root:
        raise RuntimeError(
            "Local full YOLO root was not provided. Pass --local-full-yolo-root "
            "or set COCO_LOCAL_FULL_YOLO_ROOT."
        )

    local_cropped_root = Path(args.local_cropped_root).expanduser().resolve()
    local_full_yolo_root = Path(args.local_full_yolo_root).expanduser().resolve()

    if args.output_manifest_path:
        output_manifest_path = Path(args.output_manifest_path).expanduser().resolve()
    else:
        output_manifest_path = (
            local_cropped_root.parent / "coco_pottedplant_manifest.csv"
        ).resolve()

    return local_cropped_root, local_full_yolo_root, output_manifest_path


def validate_inputs(local_cropped_root: Path, local_full_yolo_root: Path, splits):
    if not local_cropped_root.exists():
        raise FileNotFoundError(
            f"Local cropped root does not exist: {local_cropped_root}"
        )

    if not local_full_yolo_root.exists():
        raise FileNotFoundError(
            f"Local full YOLO root does not exist: {local_full_yolo_root}"
        )

    for split in splits:
        cropped_image_dir = local_cropped_root / "images" / split
        cropped_label_dir = local_cropped_root / "labels" / split
        full_image_dir = local_full_yolo_root / "images" / split
        full_label_dir = local_full_yolo_root / "labels" / split

        if not cropped_image_dir.exists():
            raise FileNotFoundError(
                f"Cropped image directory for split '{split}' does not exist: "
                f"{cropped_image_dir}"
            )

        if not cropped_label_dir.exists():
            raise FileNotFoundError(
                f"Cropped label directory for split '{split}' does not exist: "
                f"{cropped_label_dir}"
            )

        if not full_image_dir.exists():
            raise FileNotFoundError(
                f"Full-image YOLO image directory for split '{split}' does not exist: "
                f"{full_image_dir}"
            )

        if not full_label_dir.exists():
            raise FileNotFoundError(
                f"Full-image YOLO label directory for split '{split}' does not exist: "
                f"{full_label_dir}"
            )


