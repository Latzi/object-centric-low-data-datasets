# HELPERS
# ============================================================

def count_nonempty_lines(txt_path: Path) -> int:
    if not txt_path.exists():
        return 0

    with txt_path.open("r", encoding="utf-8") as f:
        return sum(1 for line in f if line.strip())


def parse_sample_id(sample_id: str):
    """
    Expected cropped filename stem from step 02:

        <source_image_id>_inst0003_crop_12_34

    Returns:
        source_image_id, selected_instance_index

    If parsing fails, selected_instance_index becomes blank.
    """
    pattern = r"^(?P<source>.+)_inst(?P<idx>\d+)_crop_(?P<x>-?\d+)_(?P<y>-?\d+)$"
    match = re.match(pattern, sample_id)

    if not match:
        return sample_id, ""

    source_image_id = match.group("source")
    selected_instance_index = int(match.group("idx"))
    return source_image_id, selected_instance_index


def parse_yolo_line(line: str):
    line = line.strip()

    if not line:
        return None

    parts = line.split()

    if len(parts) < 5:
        return None

    try:
        class_id = int(float(parts[0]))
        x_center = float(parts[1])
        y_center = float(parts[2])
        width = float(parts[3])
        height = float(parts[4])

        return {
            "class_id": class_id,
            "x_center": x_center,
            "y_center": y_center,
            "width": width,
            "height": height,
        }
    except Exception:
        return None


def get_source_image_size(cv2, local_full_yolo_root: Path, split: str, source_image_id: str):
    """
    Try to recover the original full-image dimensions from the full-image YOLO subset.
    """
    for ext in [".jpg", ".jpeg", ".png"]:
        img_path = local_full_yolo_root / "images" / split / f"{source_image_id}{ext}"

        if img_path.exists():
            img = cv2.imread(str(img_path))

            if img is not None:
                height, width = img.shape[:2]
                return width, height

    return "", ""


def get_selected_instance_box_size(
    cv2,
    local_full_yolo_root: Path,
    split: str,
    source_image_id: str,
    selected_instance_index,
):
    """
    Recover the selected source-instance bbox width/height in pixels by reading
    the corresponding source full-image YOLO label file.

    This assumes the cropped filename's instXXXX index matches the annotation
    line index used in step 02.
    """
    if selected_instance_index == "":
        return "", ""

    label_path = local_full_yolo_root / "labels" / split / f"{source_image_id}.txt"

    if not label_path.exists():
        return "", ""

    lines = [
        line.strip()
        for line in label_path.read_text(encoding="utf-8").splitlines()
        if line.strip()
    ]

    if selected_instance_index < 0 or selected_instance_index >= len(lines):
        return "", ""

    ann = parse_yolo_line(lines[selected_instance_index])

    if ann is None:
        return "", ""

    source_width, source_height = get_source_image_size(
        cv2=cv2,
        local_full_yolo_root=local_full_yolo_root,
        split=split,
        source_image_id=source_image_id,
    )

    if source_width == "" or source_height == "":
        return "", ""

    bbox_width_pixels = round(ann["width"] * source_width, 2)
    bbox_height_pixels = round(ann["height"] * source_height, 2)

    return bbox_width_pixels, bbox_height_pixels


def iter_split_files(root: Path, splits):
    for split in splits:
        image_dir = root / "images" / split
        label_dir = root / "labels" / split

        if not image_dir.exists():
            print(f"Warning: missing image dir for split '{split}': {image_dir}")
            continue

        images = sorted(
            [
                path
                for path in image_dir.iterdir()
                if path.is_file() and path.suffix.lower() in {".jpg", ".jpeg", ".png"}
            ]
        )

        for img_path in images:
            sample_id = img_path.stem
            label_path = label_dir / f"{sample_id}.txt"
            yield split, img_path, label_path


def build_manifest_rows(
    cv2,
    local_cropped_root: Path,
    local_full_yolo_root: Path,
    splits,
    args,
):
    rows = []

    for split, img_path, label_path in iter_split_files(local_cropped_root, splits):
        sample_id = img_path.stem
        source_image_id, selected_instance_index = parse_sample_id(sample_id)
        num_boxes = count_nonempty_lines(label_path)

        source_width, source_height = get_source_image_size(
            cv2=cv2,
            local_full_yolo_root=local_full_yolo_root,
            split=split,
            source_image_id=source_image_id,
        )

        selected_bbox_w, selected_bbox_h = get_selected_instance_box_size(
            cv2=cv2,
            local_full_yolo_root=local_full_yolo_root,
            split=split,
            source_image_id=source_image_id,
            selected_instance_index=selected_instance_index,
        )

        rows.append(
            {
                "sample_id": sample_id,
                "source_image_id": source_image_id,
                "selected_instance_index": selected_instance_index,
                "source_annotation_id": "",
                "source_category_id": SOURCE_CATEGORY_ID,
                "source_category_name": SOURCE_CATEGORY_NAME,
                "split": split,
                "class_id": PUBLIC_CLASS_ID,
                "class_name": PUBLIC_CLASS_NAME,
                "num_boxes": num_boxes,
                "crop_size": args.crop_size,
                "source_width": source_width,
                "source_height": source_height,
                "selected_instance_bbox_width": selected_bbox_w,
                "selected_instance_bbox_height": selected_bbox_h,
                "small_box_filter_rule": args.small_box_filter_rule,
                "random_offset_expected": args.random_offset_expected,
                "context_variability_expected": args.context_variability_expected,
                "mask_expected": args.mask_expected,
                "derived_artifact_relpath": "",
                "annotation_relpath": "",
                "filter_stage": args.filter_stage,
                "notes": (
                    "Public metadata record generated from local cropped YOLO subset"
                ),
            }
        )

    return rows


def get_manifest_fieldnames():
    return [
        "sample_id",
        "source_image_id",
        "selected_instance_index",
        "source_annotation_id",
        "source_category_id",
        "source_category_name",
        "split",
        "class_id",
        "class_name",
        "num_boxes",
        "crop_size",
        "source_width",
        "source_height",
        "selected_instance_bbox_width",
        "selected_instance_bbox_height",
        "small_box_filter_rule",
        "random_offset_expected",
        "context_variability_expected",
        "mask_expected",
        "derived_artifact_relpath",
        "annotation_relpath",
        "filter_stage",
        "notes",
    ]


def write_manifest(output_manifest_path: Path, rows):
    output_manifest_path.parent.mkdir(parents=True, exist_ok=True)

    with output_manifest_path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=get_manifest_fieldnames())
        writer.writeheader()
        writer.writerows(rows)


# ============================================================
# MAIN
# ============================================================

def main() -> None:
    args = parse_args()

    local_cropped_root, local_full_yolo_root, output_manifest_path = resolve_paths(args)

    validate_inputs(
        local_cropped_root=local_cropped_root,
        local_full_yolo_root=local_full_yolo_root,
        splits=args.splits,
    )

    cv2 = import_cv2()

    print("COCO PottedPlant public manifest builder")
    print("========================================")
    print(f"Local cropped root:   {local_cropped_root}")
    print(f"Local full YOLO root: {local_full_yolo_root}")
    print(f"Output manifest:      {output_manifest_path}")
    print(f"Splits:               {args.splits}")

    rows = build_manifest_rows(
        cv2=cv2,
        local_cropped_root=local_cropped_root,
        local_full_yolo_root=local_full_yolo_root,
        splits=args.splits,
        args=args,
    )

    write_manifest(output_manifest_path, rows)

    train_count = sum(1 for row in rows if row["split"] == "train")
    val_count = sum(1 for row in rows if row["split"] == "val")

    print("")
    print("COCO public manifest written:")
    print(output_manifest_path)
    print("")
    print("Total rows:", len(rows))
    print("Train:", train_count)
    print("Val:", val_count)


if __name__ == "__main__":
    main()