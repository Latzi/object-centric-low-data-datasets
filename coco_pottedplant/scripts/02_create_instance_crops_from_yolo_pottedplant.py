import argparse
import json
import os
import random
import shutil
from pathlib import Path

try:
    from tqdm import tqdm
except ImportError:
    def tqdm(iterable, **kwargs):
        return iterable


# ============================================================
# DEFAULT SETTINGS
# ============================================================

DEFAULT_SPLITS = ["train", "val"]

DEFAULT_CROP_SIZE = 256
DEFAULT_RANDOM_SEED = 42
DEFAULT_CLEAR_OUTPUT_ON_RUN = True

DEFAULT_MIN_BOX_WIDTH_PIXELS = 12
DEFAULT_MIN_BOX_HEIGHT_PIXELS = 12

DEFAULT_INCLUDE_ALL_INTERSECTING_BOXES = True

IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp"}

DEFAULT_YOLO_CLASS_NAME = "potted_plant"
DEFAULT_YOLO_CLASS_ID = 0


# ============================================================
# ARGUMENTS
# ============================================================

def parse_args():
    parser = argparse.ArgumentParser(
        description=(
            "Create per-instance 256x256 COCO PottedPlant crops from a local "
            "YOLO-format potted-plant subset generated by step 01."
        )
    )

    parser.add_argument(
        "--coco-root",
        default=os.environ.get("COCO_ROOT"),
        help=(
            "Path to the local COCO dataset root. The default source folder is "
            "COCO_ROOT/YOLO_pottedplant and the default output folder is "
            "COCO_ROOT/YOLO_pottedplant_cropped. You may also set the COCO_ROOT "
            "environment variable."
        ),
    )

    parser.add_argument(
        "--source-dir",
        default=None,
        help=(
            "Path to the local YOLO potted-plant dataset generated by step 01. "
            "Default: COCO_ROOT/YOLO_pottedplant."
        ),
    )

    parser.add_argument(
        "--output-dir",
        default=None,
        help=(
            "Output directory for the cropped YOLO potted-plant subset. "
            "Default: COCO_ROOT/YOLO_pottedplant_cropped."
        ),
    )

    parser.add_argument(
        "--splits",
        nargs="+",
        default=DEFAULT_SPLITS,
        choices=["train", "val"],
        help="Splits to process. Default: train val.",
    )

    parser.add_argument(
        "--crop-size",
        type=int,
        default=DEFAULT_CROP_SIZE,
        help="Square crop size in pixels. Default: 256.",
    )

    parser.add_argument(
        "--random-seed",
        type=int,
        default=DEFAULT_RANDOM_SEED,
        help="Random seed used for crop placement. Default: 42.",
    )

    parser.add_argument(
        "--min-box-width-pixels",
        type=int,
        default=DEFAULT_MIN_BOX_WIDTH_PIXELS,
        help="Minimum source bounding-box width in pixels. Default: 12.",
    )

    parser.add_argument(
        "--min-box-height-pixels",
        type=int,
        default=DEFAULT_MIN_BOX_HEIGHT_PIXELS,
        help="Minimum source bounding-box height in pixels. Default: 12.",
    )

    parser.add_argument(
        "--include-all-intersecting-boxes",
        dest="include_all_intersecting_boxes",
        action="store_true",
        default=DEFAULT_INCLUDE_ALL_INTERSECTING_BOXES,
        help=(
            "Include all potted-plant boxes that intersect the crop. "
            "This is the default."
        ),
    )

    parser.add_argument(
        "--selected-instance-only",
        dest="include_all_intersecting_boxes",
        action="store_false",
        help="Write only the selected instance box into each crop label.",
    )

    parser.add_argument(
        "--clear-output",
        dest="clear_output_on_run",
        action="store_true",
        default=DEFAULT_CLEAR_OUTPUT_ON_RUN,
        help="Clear the output directory before writing crops. This is the default.",
    )

    parser.add_argument(
        "--no-clear-output",
        dest="clear_output_on_run",
        action="store_false",
        help="Do not delete an existing output directory before running.",
    )

    parser.add_argument(
        "--yolo-class-id",
        type=int,
        default=DEFAULT_YOLO_CLASS_ID,
        help="YOLO class ID to keep and write. Default: 0.",
    )

    parser.add_argument(
        "--yolo-class-name",
        default=DEFAULT_YOLO_CLASS_NAME,
        help="YOLO class name written to classes.txt and data.yaml. Default: potted_plant.",
    )

    return parser.parse_args()


# ============================================================
# PATH RESOLUTION AND VALIDATION
# ============================================================

def resolve_paths(args):
    if args.coco_root:
        coco_root = Path(args.coco_root).expanduser().resolve()
    else:
        coco_root = None

    if args.source_dir:
        source_dir = Path(args.source_dir).expanduser().resolve()
    else:
        if coco_root is None:
            raise RuntimeError(
                "No source directory was provided. Pass --source-dir, "
                "or pass --coco-root, or set the COCO_ROOT environment variable."
            )
        source_dir = coco_root / "YOLO_pottedplant"

    if args.output_dir:
        out_dir = Path(args.output_dir).expanduser().resolve()
    else:
        if coco_root is None:
            raise RuntimeError(
                "No output directory was provided. Pass --output-dir, "
                "or pass --coco-root, or set the COCO_ROOT environment variable."
            )
        out_dir = coco_root / "YOLO_pottedplant_cropped"

    return source_dir, out_dir


def validate_inputs(source_dir, out_dir, splits):
    if not source_dir.exists():
        raise FileNotFoundError(
            f"Source YOLO dataset does not exist: {source_dir}"
        )

    source_images_root = source_dir / "images"
    source_labels_root = source_dir / "labels"

    if not source_images_root.exists():
        raise FileNotFoundError(
            f"Source images folder does not exist: {source_images_root}"
        )

    if not source_labels_root.exists():
        raise FileNotFoundError(
            f"Source labels folder does not exist: {source_labels_root}"
        )

    for split in splits:
        split_img_dir = source_images_root / split
        split_lbl_dir = source_labels_root / split

        if not split_img_dir.exists():
            raise FileNotFoundError(
                f"Source image split folder does not exist: {split_img_dir}"
            )

        if not split_lbl_dir.exists():
            raise FileNotFoundError(
                f"Source label split folder does not exist: {split_lbl_dir}"
            )

    if source_dir == out_dir:
        raise RuntimeError(
            "The source directory and output directory are the same. "
            "Use a different --output-dir."
        )


# ============================================================
# HELPERS
# ============================================================

def parse_yolo_label_file(label_path):
    anns = []

    if not label_path.exists():
        return anns

    with label_path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()

            if not line:
                continue

            parts = line.split()

            if len(parts) < 5:
                continue

            class_id = int(float(parts[0]))
            x_center = float(parts[1])
            y_center = float(parts[2])
            width = float(parts[3])
            height = float(parts[4])

            anns.append({
                "class_id": class_id,
                "x_center": x_center,
                "y_center": y_center,
                "width": width,
                "height": height,
            })

    return anns


def yolo_to_pixel_bbox(ann, img_w, img_h):
    x_center = ann["x_center"] * img_w
    y_center = ann["y_center"] * img_h
    bw = ann["width"] * img_w
    bh = ann["height"] * img_h

    x1 = x_center - bw / 2.0
    y1 = y_center - bh / 2.0
    x2 = x_center + bw / 2.0
    y2 = y_center + bh / 2.0

    return x1, y1, x2, y2


def pixel_bbox_to_yolo(x1, y1, x2, y2, img_w, img_h):
    bw = x2 - x1
    bh = y2 - y1
    x_center = x1 + bw / 2.0
    y_center = y1 + bh / 2.0

    return (
        x_center / img_w,
        y_center / img_h,
        bw / img_w,
        bh / img_h,
    )


def clip_bbox_to_crop(box, crop_x, crop_y, crop_size):
    x1, y1, x2, y2 = box

    inter_left = max(x1, crop_x)
    inter_top = max(y1, crop_y)
    inter_right = min(x2, crop_x + crop_size)
    inter_bottom = min(y2, crop_y + crop_size)

    if inter_right <= inter_left or inter_bottom <= inter_top:
        return None

    cx1 = inter_left - crop_x
    cy1 = inter_top - crop_y
    cx2 = inter_right - crop_x
    cy2 = inter_bottom - crop_y

    return cx1, cy1, cx2, cy2


def choose_crop_for_box(
    box,
    img_w,
    img_h,
    rng,
    crop_size,
    min_box_width_pixels,
    min_box_height_pixels,
):
    """
    Choose a square crop so the selected bbox lies fully inside the crop,
    with random offset when possible.
    """
    x1, y1, x2, y2 = box

    if img_w < crop_size or img_h < crop_size:
        return None

    bw = x2 - x1
    bh = y2 - y1

    if bw < min_box_width_pixels or bh < min_box_height_pixels:
        return None

    crop_x_min = max(0, int(x2 - crop_size))
    crop_x_max = min(int(x1), img_w - crop_size)

    crop_y_min = max(0, int(y2 - crop_size))
    crop_y_max = min(int(y1), img_h - crop_size)

    if crop_x_min > crop_x_max or crop_y_min > crop_y_max:
        return None

    crop_x = rng.randint(crop_x_min, crop_x_max)
    crop_y = rng.randint(crop_y_min, crop_y_max)

    return crop_x, crop_y


def ensure_dir(path):
    path.mkdir(parents=True, exist_ok=True)


def write_classes_file(out_classes_file, yolo_class_name):
    with out_classes_file.open("w", encoding="utf-8") as f:
        f.write(f"{yolo_class_name}\n")


def write_data_yaml(out_data_yaml, out_dir, yolo_class_name):
    yolo_path = str(out_dir).replace("\\", "/")

    with out_data_yaml.open("w", encoding="utf-8") as f:
        f.write(f"path: {yolo_path}\n")
        f.write("train: images/train\n")
        f.write("val: images/val\n")
        f.write("\n")
        f.write("names:\n")
        f.write(f"  0: {yolo_class_name}\n")


def import_cv2():
    try:
        import cv2
    except ImportError as exc:
        raise RuntimeError(
            "OpenCV is required for this script. Install it with: "
            "pip install opencv-python"
        ) from exc

    return cv2


# ============================================================
# MAIN
# ============================================================

def main():
    args = parse_args()

    source_dir, out_dir = resolve_paths(args)
    validate_inputs(source_dir, out_dir, args.splits)

    cv2 = import_cv2()

    source_images_root = source_dir / "images"
    source_labels_root = source_dir / "labels"

    out_images_root = out_dir / "images"
    out_labels_root = out_dir / "labels"
    out_classes_file = out_dir / "classes.txt"
    out_data_yaml = out_dir / "data.yaml"
    out_summary_json = out_dir / "crop_summary.json"

    print("COCO PottedPlant instance crop generation")
    print("=========================================")
    print(f"Source YOLO dir:     {source_dir}")
    print(f"Output crop dir:     {out_dir}")
    print(f"Splits:              {args.splits}")
    print(f"Crop size:           {args.crop_size}")
    print(f"Random seed:         {args.random_seed}")
    print(f"Clear output:        {args.clear_output_on_run}")
    print(f"Include all boxes:   {args.include_all_intersecting_boxes}")
    print(f"YOLO class ID:       {args.yolo_class_id}")
    print(f"YOLO class name:     {args.yolo_class_name}")

    if args.clear_output_on_run and out_dir.exists():
        shutil.rmtree(out_dir)

    ensure_dir(out_images_root)
    ensure_dir(out_labels_root)

    rng = random.Random(args.random_seed)

    summary = {
        "source_dir": str(source_dir),
        "output_dir": str(out_dir),
        "crop_size": args.crop_size,
        "random_seed": args.random_seed,
        "min_box_width_pixels": args.min_box_width_pixels,
        "min_box_height_pixels": args.min_box_height_pixels,
        "include_all_intersecting_boxes": args.include_all_intersecting_boxes,
        "splits": {},
    }

    for split in args.splits:
        print(f"\nProcessing split: {split}")

        src_img_dir = source_images_root / split
        src_lbl_dir = source_labels_root / split

        out_img_dir = out_images_root / split
        out_lbl_dir = out_labels_root / split

        ensure_dir(out_img_dir)
        ensure_dir(out_lbl_dir)

        image_files = sorted([
            f for f in os.listdir(src_img_dir)
            if os.path.splitext(f)[1].lower() in IMAGE_EXTENSIONS
        ])

        images_seen = 0
        instance_boxes_seen = 0
        crops_written = 0
        skipped_small_boxes = 0
        skipped_small_images = 0

        for image_name in tqdm(image_files, desc=f"Cropping {split}"):
            img_path = src_img_dir / image_name
            lbl_path = src_lbl_dir / (Path(image_name).stem + ".txt")

            img = cv2.imread(str(img_path))

            if img is None:
                continue

            img_h, img_w = img.shape[:2]
            images_seen += 1

            anns = parse_yolo_label_file(lbl_path)

            if img_w < args.crop_size or img_h < args.crop_size:
                skipped_small_images += 1
                continue

            for ann_idx, ann in enumerate(anns):
                if ann["class_id"] != args.yolo_class_id:
                    continue

                box = yolo_to_pixel_bbox(ann, img_w, img_h)
                instance_boxes_seen += 1

                bw = box[2] - box[0]
                bh = box[3] - box[1]

                if bw < args.min_box_width_pixels or bh < args.min_box_height_pixels:
                    skipped_small_boxes += 1
                    continue

                crop_origin = choose_crop_for_box(
                    box=box,
                    img_w=img_w,
                    img_h=img_h,
                    rng=rng,
                    crop_size=args.crop_size,
                    min_box_width_pixels=args.min_box_width_pixels,
                    min_box_height_pixels=args.min_box_height_pixels,
                )

                if crop_origin is None:
                    skipped_small_boxes += 1
                    continue

                crop_x, crop_y = crop_origin
                crop_img = img[
                    crop_y:crop_y + args.crop_size,
                    crop_x:crop_x + args.crop_size,
                ]

                if (
                    crop_img.shape[0] != args.crop_size
                    or crop_img.shape[1] != args.crop_size
                ):
                    continue

                crop_name = (
                    f"{Path(image_name).stem}_inst{ann_idx:04d}_"
                    f"crop_{crop_x}_{crop_y}.jpg"
                )

                crop_img_path = out_img_dir / crop_name
                crop_lbl_path = out_lbl_dir / (Path(crop_name).stem + ".txt")

                crop_lines = []

                if args.include_all_intersecting_boxes:
                    for other_ann in anns:
                        if other_ann["class_id"] != args.yolo_class_id:
                            continue

                        other_box = yolo_to_pixel_bbox(other_ann, img_w, img_h)
                        clipped = clip_bbox_to_crop(
                            other_box,
                            crop_x,
                            crop_y,
                            args.crop_size,
                        )

                        if clipped is None:
                            continue

                        cx1, cy1, cx2, cy2 = clipped
                        xcn, ycn, wn, hn = pixel_bbox_to_yolo(
                            cx1,
                            cy1,
                            cx2,
                            cy2,
                            args.crop_size,
                            args.crop_size,
                        )

                        crop_lines.append(
                            f"{args.yolo_class_id} "
                            f"{xcn:.6f} {ycn:.6f} {wn:.6f} {hn:.6f}"
                        )
                else:
                    clipped = clip_bbox_to_crop(
                        box,
                        crop_x,
                        crop_y,
                        args.crop_size,
                    )

                    if clipped is None:
                        continue

                    cx1, cy1, cx2, cy2 = clipped
                    xcn, ycn, wn, hn = pixel_bbox_to_yolo(
                        cx1,
                        cy1,
                        cx2,
                        cy2,
                        args.crop_size,
                        args.crop_size,
                    )

                    crop_lines.append(
                        f"{args.yolo_class_id} "
                        f"{xcn:.6f} {ycn:.6f} {wn:.6f} {hn:.6f}"
                    )

                cv2.imwrite(str(crop_img_path), crop_img)

                with crop_lbl_path.open("w", encoding="utf-8") as f:
                    f.write("\n".join(crop_lines))

                crops_written += 1

        summary["splits"][split] = {
            "images_seen": images_seen,
            "instance_boxes_seen": instance_boxes_seen,
            "crops_written": crops_written,
            "skipped_small_boxes": skipped_small_boxes,
            "skipped_small_images": skipped_small_images,
        }

        print(f"Split {split}:")
        print(f"  images_seen: {images_seen}")
        print(f"  instance_boxes_seen: {instance_boxes_seen}")
        print(f"  crops_written: {crops_written}")
        print(f"  skipped_small_boxes: {skipped_small_boxes}")
        print(f"  skipped_small_images: {skipped_small_images}")

    write_classes_file(out_classes_file, args.yolo_class_name)
    write_data_yaml(out_data_yaml, out_dir, args.yolo_class_name)

    with out_summary_json.open("w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    print("\nALL DONE.")
    print(f"Cropped YOLO subset written to: {out_dir}")
    print(f"classes.txt written to: {out_classes_file}")
    print(f"data.yaml written to: {out_data_yaml}")
    print(f"summary written to: {out_summary_json}")


if __name__ == "__main__":
    main()